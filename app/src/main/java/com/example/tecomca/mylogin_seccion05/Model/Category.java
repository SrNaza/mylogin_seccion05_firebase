package com.example.tecomca.mylogin_seccion05.Model;

public class Category {

    private int id;
    private String name;
    private byte[] imagen;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public byte[] getEmail() {
        return imagen;
    }

    public void setEmail(byte[] email) {
        this.imagen = email;
    }

}